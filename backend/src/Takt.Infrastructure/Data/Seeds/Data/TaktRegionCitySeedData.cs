    /// <summary>
    /// 初始化中国市级行政区种子数据
    /// </summary>
    /// <param name="serviceProvider">服务提供者</param>
    /// <param name="configId">当前数据库配置ID</param>
    /// <returns>返回插入和更新的记录数（插入数, 更新数）</returns>
    public async Task<(int InsertCount, int UpdateCount)> SeedAsync(IServiceProvider serviceProvider, string configId)
    {
        var countryRepository = serviceProvider.GetRequiredService<ITaktRepository<TaktRegionCountry>>();
        var provinceRepository = serviceProvider.GetRequiredService<ITaktRepository<TaktRegionProvince>>();
        var cityRepository = serviceProvider.GetRequiredService<ITaktRepository<TaktRegionCity>>();

        var china = await countryRepository.GetAsync(x => x.RegionCode == "CN");
        if (china == null)
        {
            return (0, 0);
        }

        var provinces = await provinceRepository.FindAsync(x => x.CountryId == china.Id);
        var provinceByCode = provinces.ToDictionary(x => x.RegionCode, x => x.Id);

        int insertCount = 0;
        int updateCount = 0;
        var list = GetCityList();

        foreach (var c in list)
        {
            if (!provinceByCode.TryGetValue(c.ProvinceCode, out var provinceId))
            {
                continue;
            }

            var existing = await cityRepository.GetAsync(x => x.ProvinceId == provinceId && x.RegionCode == c.RegionCode);

            if (existing == null)
            {
                var entity = new TaktRegionCity
                {
                    ProvinceId = provinceId,
                    RegionCode = c.RegionCode,
                    RegionName = c.RegionName,
                    ShortName = c.ShortName,
                    RegionLevel = 3,
                    OrderNum = c.OrderNum,
                    IsDeleted = 0
                };
                await cityRepository.CreateAsync(entity);
                insertCount++;
            }
            else
            {
                existing.RegionName = c.RegionName;
                existing.ShortName = c.ShortName;
                existing.OrderNum = c.OrderNum;
                await cityRepository.UpdateAsync(existing);
                updateCount++;
            }
        }

        return (insertCount, updateCount);
    }

