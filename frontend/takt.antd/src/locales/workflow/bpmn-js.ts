/**
 * 返回 bpmn-js translate 模块所需的函数：(key) => string
 */
export function getBpmnJsTranslate(locale: string): (key: string) => string {
  const dict = localeMap[locale] ?? bpmnJsEn
  return (key: string) => dict[key] ?? key
}
